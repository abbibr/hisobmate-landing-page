        // FAQ Toggle Functionality
        document.querySelectorAll('.faq-toggle').forEach(button => {
            button.addEventListener('click', () => {
                const faqItem = button.closest('.faq-item');
                const faqContent = faqItem.querySelector('.faq-content');
                const icon = button.querySelector('i');

                // Close other open items
                document.querySelectorAll('.faq-item').forEach(item => {
                    if (item !== faqItem) {
                        item.querySelector('.faq-content').style.maxHeight = '0';
                        item.querySelector('i').style.transform = 'rotate(0deg)';
                    }
                });

                // Toggle current item
                if (faqContent.style.maxHeight === '0px' || !faqContent.style.maxHeight) {
                    faqContent.style.maxHeight = faqContent.scrollHeight + 'px';
                    icon.style.transform = 'rotate(180deg)';
                } else {
                    faqContent.style.maxHeight = '0';
                    icon.style.transform = 'rotate(0deg)';
                }
            });
        });

        // Update FAQ translations when language changes
        function updateFaqTranslations() {
            document.querySelectorAll('#faq [data-en]').forEach(el => {
                const text = el.getAttribute(`data-${currentLang}`);
                if (text) el.textContent = text;
            });
        }
